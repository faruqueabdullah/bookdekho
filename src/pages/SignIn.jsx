import { useState } from "react";
import { UseFirebasecontext } from "../firebase/firebase";

export default function SignIn() {
  const [email, setEmail] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const { checkUser, loginUserwithGoogle } = UseFirebasecontext();

  const handleClick = async (e) => {
    try {
      e.preventDefault();
      if (!email.trim() || !password.trim()) return;
      await checkUser(email, password);
      setEmail("");
      setPassword("");
    } catch (error) {
      if (error.message === "Firebase: Error (auth/invalid-credential).") {
        setError("Email or password incorrect");
      }
      setEmail("");
      setPassword("");
    }
  };
  const loginwithGoogleAccount= ()=>{
    loginUserwithGoogle()
  }

  return (
    <div id="container" className="w-full min-h-screen bg-blue-950">
      <div className="flex min-h-full flex-col justify-center px-6 py-12 lg:px-8">
        <div className="sm:mx-auto sm:w-full sm:max-w-sm">
          <h2 className="mt-10 text-center text-2xl/9 font-bold tracking-tight text-white">
            Login
          </h2>
        </div>

        <div className="mt-10 sm:mx-auto sm:w-full sm:max-w-sm">
          <form onSubmit={handleClick} className="space-y-6">
            <div>
              <label
                htmlFor="email"
                className="block text-sm/6 font-medium text-gray-100"
              >
                Email address
              </label>
              <div className="mt-2">
                <input
                  id="email"
                  type="email"
                  name="email"
                  value={email}
                  onChange={(e) => setEmail(e.target.value)}
                  required
                  autoComplete="email"
                  className="block w-full rounded-md bg-white/5 px-3 py-1.5 text-base text-white outline-1 -outline-offset-1 outline-white/10 placeholder:text-gray-500 focus:outline-2 focus:-outline-offset-2 focus:outline-indigo-500 sm:text-sm/6"
                />
              </div>
            </div>

            <div>
              <div className="flex items-center justify-between">
                <label
                  htmlFor="password"
                  className="block text-sm/6 font-medium text-gray-100"
                >
                  Password
                </label>
              </div>
              <div className="mt-2">
                <input
                  id="password"
                  type="password"
                  name="password"
                  value={password}
                  onChange={(e) => setPassword(e.target.value)}
                  required
                  autoComplete="current-password"
                  className="block w-full rounded-md bg-white/5 px-3 py-1.5 text-base text-white outline-1 -outline-offset-1 outline-white/10 placeholder:text-gray-500 focus:outline-2 focus:-outline-offset-2 focus:outline-indigo-500 sm:text-sm/6"
                />
              </div>
            </div>

            <div>
              <button
                type="submit"
                className="cursor-pointer flex w-full justify-center rounded-md bg-indigo-500 px-3 py-1.5 text-sm/6 font-semibold text-white hover:bg-indigo-400 focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-500"
              >
                Login
              </button>
            </div>
          </form>
          <p className="mt-4 text-center text-sm/6 text-red-500">{error}</p>
          <p className="mt-4 text-center text-sm/6 font-bold text-white"> OR</p>
          <button
            onClick={loginwithGoogleAccount}
            type="submit"
            className="cursor-pointer flex w-full justify-center rounded-md bg-transparent px-3 py-1.5 text-sm/6 font-semibold text-white focus-visible:outline-2 focus-visible:outline-offset-2 focus-visible:outline-indigo-500"
          >
            Login with Google
          </button>
        </div>
      </div>
    </div>
  );
}
